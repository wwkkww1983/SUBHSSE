        protected void Page_Load(object sender, EventArgs e)
        {
            // 表头过滤
            //FilterDataRowItem = FilterDataRowItemImplement;
            if (!IsPostBack)
            {
                ////权限按钮方法
                this.GetButtonPower();
                this.btnNew.OnClientClick = Window1.GetShowReference("AccidentReportEdit.aspx") + "return false;";
                if (this.CurrUser != null && this.CurrUser.PageSize.HasValue)
                {
                    Grid1.PageSize = this.CurrUser.PageSize.Value;
                }
                this.ddlPageSize.SelectedValue = Grid1.PageSize.ToString();
                // 绑定表格
                BindGrid();
            }
        }

        /// <summary>
        /// 绑定数据
        /// </summary>
        private void BindGrid()
        {
            string strSql = @"SELECT S.AccidentReportId,S.ProjectId,P.ProjectName,S.UnitId,u.UnitName,S.WorkArea,S.CompileDate,S.AttachUrl,S.AccidentDescription,S.Casualties,"
                        + @"(CASE WHEN S.States = " + BLL.Const.State_0 + " OR S.States IS NULL THEN '待['+OperateUser.UserName+']提交' WHEN S.States =  " + BLL.Const.State_2 + " THEN '审核/审批完成' ELSE '待['+OperateUser.UserName+']办理' END) AS  FlowOperateName"
                        + @" FROM dbo.ProjectAccident_AccidentReport AS S"
                        + @" LEFT JOIN dbo.Base_Project AS P ON P.ProjectId=S.ProjectId"
                        + @" LEFT JOIN dbo.Base_Unit AS U ON U.UnitId=S.UnitId"
                        + @" LEFT JOIN Sys_FlowOperate AS FlowOperate ON S.AccidentReportId = FlowOperate.DataId AND FlowOperate.IsClosed <> 1"                          
                        + @" LEFT JOIN Sys_User AS OperateUser ON FlowOperate.OperaterId = OperateUser.UserId WHERE 1= 1 ";

            List<SqlParameter> listStr = new List<SqlParameter>();           
            if (!string.IsNullOrEmpty(this.txtName.Text.Trim()))
            {
                strSql += " AND (P.ProjectName LIKE @Name OR u.UnitName LIKE @Name OR S.WorkArea LIKE @Name OR S.AccidentDescription LIKE @Name OR S.Casualties LIKE @Name)";
                listStr.Add(new SqlParameter("@Name", "%" + this.txtName.Text.Trim() + "%"));
            }
            SqlParameter[] parameter = listStr.ToArray();
            DataTable tb = SQLHelper.GetDataTableRunText(strSql, parameter);

            Grid1.RecordCount = tb.Rows.Count;
            tb = GetFilteredTable(Grid1.FilteredData, tb);
            var table = this.GetPagedDataTable(Grid1, tb);
            Grid1.DataSource = table;
            Grid1.DataBind();
        }
        
