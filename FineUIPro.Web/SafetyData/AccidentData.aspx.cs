    public partial class AccidentData : PageBase
    {
        #region 定义项
        /// <summary>
        /// 清单主键
        /// </summary>
        public string ResetManHoursId
        {
            get
            {
                return (string)ViewState["ResetManHoursId"];
            }
            set
            {
                ViewState["ResetManHoursId"] = value;
            }
        }
        #endregion

        #region 加载页面
        /// <summary>
        /// 加载页面
        /// </summary>
        /// <param name="sender"></param>
        /// <param name="e"></param>
        protected void Page_Load(object sender, EventArgs e)
        {
            if (!IsPostBack)
            {
                ////权限按钮方法
                this.GetButtonPower();
                this.btnNew.OnClientClick = Window1.GetShowReference("AccidentDataEdit.aspx") + "return false;";
                ddlPageSize.SelectedValue = Grid1.PageSize.ToString();
                // 绑定表格
                BindGrid();
            }
        }

        /// <summary>
        /// 绑定数据
        /// </summary>
        private void BindGrid()
        {
            string strSql = "select ResetManHours.ResetManHoursId,ResetManHours.AccidentDate,Project.ProjectName,AccidentType.ConstText AS AccidentTypeName,ResetManHours.Abstract,ResetManHours.BeforeManHours,ResetManHours.ProjectManager,ResetManHours.HSSEManager"
                          + @" from Manager_ResetManHours AS ResetManHours "
                          + @" LEFT JOIN Base_Project AS Project ON ResetManHours.ProjectId=Project.ProjectId "
                          + @" LEFT JOIN Sys_Const AS AccidentType ON AccidentType.ConstValue = ResetManHours.AccidentTypeId and AccidentType.GroupId='AccidentReportRegistration' WHERE 1=1 ";
            List<SqlParameter> listStr = new List<SqlParameter>();
            if (!string.IsNullOrEmpty(this.txtDate.Text.Trim()))
            {
                strSql += " AND ResetManHours.AccidentDate = @ResetManHoursDate";
                listStr.Add(new SqlParameter("@ResetManHoursDate", this.txtDate.Text.Trim()));
            }
            strSql += " order by ResetManHours.AccidentDate desc";
            SqlParameter[] parameter = listStr.ToArray();
            DataTable tb = SQLHelper.GetDataTableRunText(strSql, parameter);
            Grid1.RecordCount = tb.Rows.Count;
            tb = GetFilteredTable(Grid1.FilteredData, tb);
            var table = this.GetPagedDataTable(Grid1, tb);

            Grid1.DataSource = table;
            Grid1.DataBind();
        }
        #endregion

